<script>
  export default {
    name: 'CardsModal',
    props: {
      cardsModalData: Array,
    },
    data: function() {
      return {
        modalStatus: null,
        currentItem: 0,
        itemChanging: false,
      }
    },
    computed: {
      
    },
    methods: {
      openModal: function(target) {
        this.currentItem = target;
        this.modalStatus = true;
      },
      closeModal: function() {
        this.modalStatus = false;
      },
      getNextIndex: function() {
        if (this.currentItem < this.cardsModalData.length - 1) {
          return this.currentItem + 1;
        } else {
          return 0;
        }
      },
      goToStep: function(target) {
        this.itemChanging = true;
        setTimeout(() => {
          this.currentItem = target;
          setTimeout(() => {
            this.itemChanging = false;
          }, 200);
        }, 500);
      }
    },
    mounted () {
      
    }
  }
</script>

